 <h4  class="text-xl">
    The following checklist of common documents is a must for applying (or getting) a visa to any country:
  
</h4>



  <ul  style="list-style-type:circle;">





    <li>
        Passport (current and old)
    </li>
    <li>Passport size photographs (at least 2)</li>
    <li>Birth Certificate (with name of both parents)</li>
    <li>
        Visa forms from the respective consulates
    </li>
    <li>Purpose of visit</li>

    <li>Financial Documents (Bank Statements/Taxation Documents)</li>
    <li>  Marriage Certificate (if applicable)</li>
    <li>  Professional and academic documents</li>
  </ul>

